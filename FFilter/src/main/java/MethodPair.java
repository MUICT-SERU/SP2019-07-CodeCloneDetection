public class MethodPair {
    private Method method1;
    private Method method2;

    public MethodPair(Method m1,Method m2){
        this.method1 = m1;
        this.method2 = m2;
    }

    public Method getMethod1() {
        return method1;
    }

    public Method getMethod2() {
        return method2;
    }
}

